public class modifyvariables {
   public static void main(String[] args) {
    int x;
    x=5;
    System.out.println(x);
    x=x+7;
    System.out.println(x);
    x=x-7;
    System.out.println(x);
    x=x/5;
    System.out.println(x);
    x=x*7;
    System.out.println(x);
    x=x-18;
    System.out.println(x);
   } 
}
